#include <unistd.h>

void	ft_print_comb2(void)
{
	int	n1;
	int	n2;

	n1 = 0;
	n2 = 0;
	while (n1 <= 9)
	{
		while (n2 <= 8)
		{
			write (1, &n1, 1);
			write (1, &n2, 1);
			n2++;
		}
		if (n2 == 9)
		{
			n2 = 0;
		}
		
		write (1, &n1, 1);
		write (1, &n2, 1);
		n1++;

	}
}